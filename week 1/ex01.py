'''
Inputs temperature in Celsius, converts and outputs as Fahrenheit.
'''

tempC = float(input("Enter a temperature in Celsius: "))

tempF = tempC * 1.8 + 32

print('\n', "The equivalent of ", tempC, " degree Celsius is ", tempF, " Fahrenheit.")
